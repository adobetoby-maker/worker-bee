#!/usr/bin/env node
/**
 * pull-manage-config.mjs
 *
 * Pulls environment variables for a site from manage.worker-bee.app's Supabase
 * database and writes them to .env.local in the current working directory.
 *
 * Usage (from any project root):
 *   SITE_ID=<uuid> node scripts/pull-manage-config.mjs
 *
 * Optional flags:
 *   --dry-run        Print the .env.local contents without writing the file
 *   --push-vercel    Also push each var to Vercel (requires VERCEL_TOKEN in env)
 *   --only-filled    Skip keys with no value (default: include empty keys as comments)
 *
 * Required env:
 *   SITE_ID                      UUID of the site in manage-worker-bee
 *   MANAGE_SUPABASE_URL          Supabase project URL for manage-worker-bee
 *   MANAGE_SUPABASE_SERVICE_KEY  Service role key for manage-worker-bee Supabase
 *
 * These three vars should be in the project's own .env.local or in CI secrets.
 * They are meta-credentials — they let a project pull its own config.
 *
 * Copy this file to any project's scripts/ directory.
 */

import fs from 'fs'
import path from 'path'
import { createClient } from '@supabase/supabase-js'

// ── Config ──────────────────────────────────────────────────────────────────
const SITE_ID   = process.env.SITE_ID
const SUPABASE_URL = process.env.MANAGE_SUPABASE_URL ?? 'https://qnrkifdbkcbacgznoabs.supabase.co'
const SERVICE_KEY  = process.env.MANAGE_SUPABASE_SERVICE_KEY

const DRY_RUN     = process.argv.includes('--dry-run')
const PUSH_VERCEL = process.argv.includes('--push-vercel')
const ONLY_FILLED = process.argv.includes('--only-filled')

// ── Validation ───────────────────────────────────────────────────────────────
if (!SITE_ID) {
  console.error('❌  SITE_ID env var is required.')
  console.error('    Usage: SITE_ID=<uuid> node scripts/pull-manage-config.mjs')
  process.exit(1)
}

if (!SERVICE_KEY) {
  console.error('❌  MANAGE_SUPABASE_SERVICE_KEY env var is required.')
  console.error('    Add it to .env.local (this project) — it is not the site\'s own service key.')
  process.exit(1)
}

// ── Fetch ────────────────────────────────────────────────────────────────────
const supabase = createClient(SUPABASE_URL, SERVICE_KEY, { auth: { persistSession: false } })

const { data: configs, error } = await supabase
  .from('project_configs')
  .select('key, value, category, is_secret, description')
  .eq('site_id', SITE_ID)
  .order('category')
  .order('key')

if (error) {
  console.error('❌  Supabase query failed:', error.message)
  process.exit(1)
}

if (!configs || configs.length === 0) {
  console.warn('⚠️   No config keys found for SITE_ID=' + SITE_ID)
  console.warn('     Open manage.worker-bee.app/sites/<id>/config and add some keys first.')
  process.exit(0)
}

// ── Build .env.local content ─────────────────────────────────────────────────
const categories = [...new Set(configs.map(c => c.category))]
const lines = ['# Generated by pull-manage-config.mjs — DO NOT COMMIT', '# Source: manage.worker-bee.app', '']

for (const cat of categories) {
  const group = configs.filter(c => c.category === cat)
  lines.push(`# ── ${cat.toUpperCase()} ${'─'.repeat(Math.max(0, 50 - cat.length))}`)

  for (const c of group) {
    if (ONLY_FILLED && !c.value) continue
    if (c.description) lines.push(`# ${c.description}`)
    if (c.value) {
      lines.push(`${c.key}=${c.value}`)
    } else {
      lines.push(`# ${c.key}=`)
    }
  }
  lines.push('')
}

const envContent = lines.join('\n')

// ── Output ────────────────────────────────────────────────────────────────────
if (DRY_RUN) {
  console.log('── DRY RUN: .env.local would contain ─────────────────────────────')
  console.log(envContent)
  console.log('───────────────────────────────────────────────────────────────────')
  process.exit(0)
}

const envPath = path.join(process.cwd(), '.env.local')
fs.writeFileSync(envPath, envContent, 'utf8')

const filled  = configs.filter(c => c.value).length
const total   = configs.length
console.log(`✅  .env.local written — ${filled}/${total} keys filled`)
console.log(`    Path: ${envPath}`)

// ── Optional: push to Vercel ──────────────────────────────────────────────────
if (PUSH_VERCEL) {
  const VERCEL_TOKEN      = process.env.VERCEL_TOKEN
  const VERCEL_PROJECT_ID = process.env.VERCEL_PROJECT_ID
  const VERCEL_TEAM_ID    = process.env.VERCEL_TEAM_ID // optional

  if (!VERCEL_TOKEN || !VERCEL_PROJECT_ID) {
    console.warn('⚠️   --push-vercel requires VERCEL_TOKEN and VERCEL_PROJECT_ID in env. Skipping.')
  } else {
    console.log('\n🚀  Pushing to Vercel...')
    const filledConfigs = configs.filter(c => c.value)

    let pushed = 0
    for (const c of filledConfigs) {
      const target = ['production', 'preview', 'development']
      const body = {
        key: c.key,
        value: c.value,
        type: 'encrypted',
        target,
      }
      const qs = VERCEL_TEAM_ID ? `?teamId=${VERCEL_TEAM_ID}` : ''
      const res = await fetch(`https://api.vercel.com/v10/projects/${VERCEL_PROJECT_ID}/env${qs}`, {
        method: 'POST',
        headers: {
          Authorization: `Bearer ${VERCEL_TOKEN}`,
          'Content-Type': 'application/json',
        },
        body: JSON.stringify(body),
      })

      if (res.ok) {
        pushed++
        console.log(`  ✓ ${c.key}`)
      } else {
        const err = await res.json().catch(() => ({}))
        // 409 means it already exists — treat as success
        if (res.status === 409) {
          // Update via PATCH
          const list = await fetch(
            `https://api.vercel.com/v10/projects/${VERCEL_PROJECT_ID}/env${qs}`,
            { headers: { Authorization: `Bearer ${VERCEL_TOKEN}` } }
          ).then(r => r.json())

          const existing = list.envs?.find((e) => e.key === c.key)
          if (existing) {
            const patch = await fetch(
              `https://api.vercel.com/v10/projects/${VERCEL_PROJECT_ID}/env/${existing.id}${qs}`,
              {
                method: 'PATCH',
                headers: { Authorization: `Bearer ${VERCEL_TOKEN}`, 'Content-Type': 'application/json' },
                body: JSON.stringify({ value: c.value, type: 'encrypted', target }),
              }
            )
            if (patch.ok) {
              pushed++
              console.log(`  ↺ ${c.key} (updated)`)
            } else {
              console.warn(`  ✗ ${c.key} — PATCH failed`)
            }
          }
        } else {
          console.warn(`  ✗ ${c.key} — ${err.error?.message ?? res.status}`)
        }
      }
    }

    console.log(`\n  ${pushed}/${filledConfigs.length} vars pushed to Vercel.`)
    console.log('  Trigger a new deployment to apply them: vercel --prod')
  }
}
